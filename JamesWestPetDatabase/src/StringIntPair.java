class StringIntPair
{
    private int n = 0;
    private String str = "";

    StringIntPair(int n, String str)
    {
        this.n = n;
        this.str = str;
    }

    public int getN()
    {
        return n;
    }

    public void setN(int n)
    {
        this.n = n;
    }

    public String getStr()
    {
        return str;
    }

    public void setStr(String str)
    {
        this.str = str;
    }
}
